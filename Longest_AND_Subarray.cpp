#include <bits/stdc++.h>
using namespace std;
#define ll long long int
#define endl '\n'

ll getMsb(ll n)
{
    if (n == 0)
        return 0;

    ll msb = 0;
    n = n / 2;
    while (n != 0)
    {
        n = n / 2;
        msb++;
    }

    return (msb);
}

int main()
{
    ios::sync_with_stdio(0);
    cin.tie(0);
    cout.tie(0);
    ll t;
    cin >> t;
    while (t--)
    {
        ll n;
        cin >> n;

        ll msb = getMsb(n);

        ll a = 0;
        ll b = 0;
        while (b < msb)
        {
            a += pow(2, b);
            b++;
        }

        ll ans1 = n - a;

        ll c = a - pow(2, (msb - 1));

        ll ans2 = a - c;

        // cout << "msb: " << msb << " a: " << a << " b: " << b << " c: " << c << " ans1: " << ans1 << " ans2: " << ans2 << endl;
        cout << max(ans1, ans2) << endl;
    }
    return 0;
}

